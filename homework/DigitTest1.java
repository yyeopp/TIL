
public class DigitTest1 {
	public static void main(String[] args) {
		int i = 1;
		while (i<=5) {
			System.out.print(i+"\t");
			i ++;
		}
		System.out.println();
		System.out.print("\t");
		while (i<=9) {
			System.out.print(i+"\t");
			i++;
		}
		System.out.println();
		System.out.print("\t\t");
		while (i<=12) {
			System.out.print(i+"\t");
			i ++;
		}
		System.out.println();
		System.out.print("\t\t\t");
		while (i<=14) {
			System.out.print(i+"\t");
			i++;
		}
		System.out.println();
		System.out.print("\t\t\t\t");
		while (i<=15) {
			System.out.print(i+"\t");
			i++;
		}
		
	}
}
